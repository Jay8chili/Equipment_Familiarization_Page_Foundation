        // ══════════════════════════════════════════════════════════════════════
        //  BUILD — DETECT
        // ══════════════════════════════════════════════════════════════════════

        private DetectInteraction BuildDetectInteraction(
            SimToolDetectData d, string stepName, string interName,
            Transform parent, Dictionary<GameObject, Interactions> built)
        {
            // ── Change 4: If no explicit source is set but detect objects are
            // filled (grab mode only), use the first detect object as the implicit source.
            if (d.detectMode == DetectInteractionMode.DetectWithGrab
                && !d.separateSources && d.combinedSource == null
                && d.detectObjects.Count > 0 && d.detectObjects[0] != null)
            {
                Debug.Log($"[Simulation Step Wizard] Detect '{interName}': No source object set — " +
                          "using first Object To Detect as the implicit source.");
                d.combinedSource = d.detectObjects[0];
            }

            string err = ValidateDetectData(d, isGaze: false);
            if (err != null)
            {
                Debug.LogWarning($"[Simulation Step Wizard] Detect for '{stepName}' skipped: {err}");
                return null;
            }

            // meshObj drives collider/visual sizing; positionObj sets world position.
            GameObject meshObj = d.separateSources ? d.meshSource : d.combinedSource;
            GameObject positionObj = d.separateSources ? d.positionSource : d.combinedSource;

            // NOTE: Detect interactions are never deduplicated — every step always
            // gets its own freshly built DetectInteraction GO, regardless of whether
            // another step references the same mesh source.

            // ── 1. Instantiate the detect prefab ──────────────────────────────
            GameObject prefab = _wizard.detectPrefab;
            GameObject go;

            if (prefab != null)
            {
                go = (GameObject)PrefabUtility.InstantiatePrefab(prefab);
                Undo.RegisterCreatedObjectUndo(go, "Create Detect GO");
            }
            else
            {
                go = new GameObject();
                Undo.RegisterCreatedObjectUndo(go, "Create Detect GO");
                Debug.LogWarning(
                    $"[Simulation Step Wizard] No Detect Prefab assigned — " +
                    "a bare GameObject was created. Assign the prefab in Window 1.");
            }

            go.name = interName;

            // ── 2. Parent under detectParent ──────────────────────────────────
            if (parent != null)
            {
                if (PrefabUtility.IsPartOfPrefabAsset(parent.gameObject))
                    Debug.LogWarning(
                        $"[Simulation Step Wizard] Detect Parent '{parent.name}' is a prefab asset. " +
                        "Parenting skipped — assign a scene instance as the parent instead.");
                else
                    go.transform.SetParent(parent, worldPositionStays: false);
            }

            // ── 3. Position from source object ────────────────────────────────
            go.transform.position = positionObj.transform.position;
            go.transform.localScale = Vector3.one;

            // ── 4. Collider on the prefab root (go), sized from the mesh source ─
            ApplyColliderFromMeshSource(go, meshObj, d.colliderMode, d.voxelSettings);

            // ── 4b. Visual overlay with shell offset ──────────────────────────
            AddMeshVisualOverlay(go, meshObj, _wizard.detectPrefab, interName,
                _wizard.detectGazeShellOffset);

            // ── 4c. Auto-size the icon UniformScaleCompensator on the FIRST
            //        child of go, based on the visual mesh just placed on go.
            {
                var mfNow = go.GetComponent<MeshFilter>();
                AutoSizeIconCompensator(go, mfNow != null ? mfNow.sharedMesh : null, childIndex: 0);
            }

            // ── 5. Wire DetectInteraction component ───────────────────────────
            // Use the one already on the prefab; add only if missing.
            var detect = go.GetComponent<DetectInteraction>()
                      ?? go.GetComponentInChildren<DetectInteraction>(includeInactive: true)
                      ?? go.AddComponent<DetectInteraction>();

            // ── Populate ObjectsToBeDetectedList ──────────────────────────────
            UpdateDetectList(detect, d, stepName, interName, built);

            EditorUtility.SetDirty(go);
            Debug.Log($"[Simulation Step Wizard] Built Detect '{go.name}' for step '{stepName}'.");
            return detect;
        }

        // ── UpdateDetectList ──────────────────────────────────────────────────
        // Writes ObjectsToBeDetectedList on an existing DetectInteraction.
        // Called both when building fresh AND when reusing a cached detect GO
        // with a different step's config (different detect objects / mode).

        private void UpdateDetectList(
            DetectInteraction detect,
            SimToolDetectData d,
            string stepName,
            string interName,
            Dictionary<GameObject, Interactions> built)
        {
            var so = new SerializedObject(detect);
            var list = so.FindProperty("ObjectsToBeDetectedList");
            list.ClearArray();

            if (d.detectMode == DetectInteractionMode.DetectWithHand)
            {
                // Assign both index fingers — no grabs.
                var fingers = new List<GameObject>
                {
                    _wizard.leftIndexFinger,
                    _wizard.rightIndexFinger
                };

                int slot = 0;
                foreach (var finger in fingers)
                {
                    if (finger == null)
                    {
                        Debug.LogWarning(
                            $"[Simulation Step Wizard] Detect '{interName}': a finger is not assigned " +
                            "in Window 1 — assign both Left and Right Index Finger objects.");
                        continue;
                    }
                    list.InsertArrayElementAtIndex(slot);
                    list.GetArrayElementAtIndex(slot).objectReferenceValue = finger;
                    slot++;
                }

                Debug.Log($"[Simulation Step Wizard] Detect '{interName}': " +
                          $"assigned {slot} finger(s) to ObjectsToBeDetectedList.");
            }
            else
            {
                // Detect With Grab — single pass over detect objects.
                int slotIdx = 0;
                Transform grabParent = _wizard.grabParent;

                for (int i = 0; i < d.detectObjects.Count; i++)
                {
                    GameObject dObj = d.detectObjects[i];
                    if (dObj == null) continue;

                    UnityEngine.Object refToWire;

                    if (built.TryGetValue(dObj, out Interactions alreadyBuilt))
                    {
                        refToWire = alreadyBuilt.gameObject;
                        Debug.Log($"[Simulation Step Wizard] Detect object '{dObj.name}' " +
                                  $"reusing existing interaction '{alreadyBuilt.gameObject.name}'.");
                    }
                    else if (d.detectObjectsAreGrabbable)
                    {
                        var gd = new SimToolGrabData
                        {
                            separateSources = false,
                            combinedSource = dObj,
                            colliderMode = d.colliderMode,
                            voxelSettings = d.voxelSettings
                        };

                        string grabName = $"{interName}_Grab_{i + 1}";
                        var grab = BuildGrabInteraction(gd, stepName, grabName, grabParent, built);
                        refToWire = grab != null ? (UnityEngine.Object)grab.gameObject : dObj;

                        if (grab != null)
                            Debug.Log($"[Simulation Step Wizard] Built GrabInteraction '{grabName}' " +
                                      $"for detect object '{dObj.name}'.");
                    }
                    else
                    {
                        refToWire = dObj;
                    }

                    list.InsertArrayElementAtIndex(slotIdx);
                    list.GetArrayElementAtIndex(slotIdx).objectReferenceValue = refToWire;
                    slotIdx++;
                }
            }

            so.ApplyModifiedProperties();
        }


