        // ── Clear all parent transforms (called from Window 1) ────────────────

        private void ClearAllParentsFromWizard()
        {
            // Build parent list — States Parent falls back to SimulationManager root.
            Transform statesRoot = statesParent != null ? statesParent
                : (simulationManager != null ? simulationManager.transform : null);

            var parents = new (string label, Transform t)[]
            {
                ("States Parent",  statesRoot),
                ("Detect Parent",  detectParent),
                ("Gaze Parent",    gazeParent),
                ("Grab Parent",    grabParent),
                ("Idle Parent",    idleParent),
                ("UI Parent",      uiParent),
            };

            // Count total children.
            int total = 0;
            foreach (var (_, t) in parents)
                if (t != null) total += t.childCount;

            if (total == 0)
            {
                EditorUtility.DisplayDialog(
                    "Nothing to Clear",
                    "All parent transforms are already empty.",
                    "OK");
                return;
            }

            // Per-parent summary for the warning.
            var lines = new System.Text.StringBuilder();
            foreach (var (label, t) in parents)
            {
                if (t == null || t.childCount == 0) continue;
                lines.AppendLine($"  • {label}  ({t.childCount} child(ren))  →  {t.name}");
            }

            // Warning popup — OK deletes, Cancel / ✕ does nothing.
            bool confirmed = EditorUtility.DisplayDialog(
                "⚠  Warning — You Are Deleting Scene Objects",
                $"You are about to permanently delete {total} child GameObject(s):\n\n" +
                lines.ToString() +
                "\nThis will also reset the Step Wizard window.\n\n" +
                "Click  OK  to delete everything.\n" +
                "Click  Cancel  (or the ✕ button) to go back safely.",
                "OK",
                "Cancel");

            if (!confirmed) return;

            // Delete under a single undoable group.
            Undo.SetCurrentGroupName("Clear All Parent Transforms");
            int undoGroup = Undo.GetCurrentGroup();

            int deleted = 0;
            foreach (var (label, t) in parents)
            {
                if (t == null) continue;
                var children = new List<GameObject>();
                for (int i = 0; i < t.childCount; i++)
                    children.Add(t.GetChild(i).gameObject);
                foreach (var child in children)
                {
                    if (child == null) continue;
                    Undo.DestroyObjectImmediate(child);
                    deleted++;
                }
                if (children.Count > 0)
                    Debug.Log($"[Simulation Step Wizard] Cleared {children.Count} child(ren) from '{label}'.");
            }

            Undo.CollapseUndoOperations(undoGroup);
            EditorSceneManager.MarkSceneDirty(EditorSceneManager.GetActiveScene());

            // Reset Window 2 so it no longer shows stale generated data.
            var win2 = GetWindow<SimulationStepConfigWindow>();
            if (win2 != null)
            {
                win2._steps = null;
                win2._languages = null;
                win2._justGenerated = false;
                win2.Repaint();
            }

            EditorUtility.DisplayDialog(
                "Clear Complete",
                $"Deleted {deleted} GameObject(s).\n\nYou can undo with Ctrl+Z before saving the scene.",
                "OK");

            Repaint();
        }

        // ── TSV path row ──────────────────────────────────────────────────────

        private void DrawTsvRow()
        {
            EditorGUILayout.BeginHorizontal();
            string label = string.IsNullOrEmpty(_tsvPath)
                ? "No file selected"
                : Path.GetFileName(_tsvPath);
            EditorGUILayout.LabelField(label, EditorStyles.miniLabel, GUILayout.ExpandWidth(true));
            if (GUILayout.Button("Browse…", GUILayout.Width(76)))
            {
                string p = EditorUtility.OpenFilePanel(
                    "Select TSV File", Application.dataPath, "tsv,txt,csv");
                if (!string.IsNullOrEmpty(p)) { _tsvPath = p; _tsvError = string.Empty; }
            }
            EditorGUILayout.EndHorizontal();
        }

        // ── Load TSV and open step window ─────────────────────────────────────

        private void TryLoadAndOpenStepWindow()
        {
            _tsvError = string.Empty;

            string[] lines;
            try { lines = File.ReadAllLines(_tsvPath); }
            catch (Exception ex) { _tsvError = $"Cannot read file: {ex.Message}"; return; }

            if (lines.Length < 2)
            {
                _tsvError = "TSV needs at least a header row and one data row.";
                return;
            }

            string[] headers = lines[0].Split('\t');
            if (headers.Length < 2)
            {
                _tsvError = "TSV needs at least 2 columns: StepName + one language column.";
                return;
            }

            var languages = new List<string>();
            for (int c = 1; c < headers.Length; c++)
                languages.Add(headers[c].Trim());

            var steps = new List<SimToolStepData>();
            for (int r = 1; r < lines.Length; r++)
            {
                if (string.IsNullOrWhiteSpace(lines[r])) continue;
                string[] cols = lines[r].Split('\t');

                var step = new SimToolStepData
                {
                    stepName = cols.Length > 0 ? cols[0].Trim() : $"Step {r}"
                };
                for (int c = 1; c < headers.Length; c++)
                    step.languageTexts.Add(c < cols.Length ? cols[c].Trim() : string.Empty);

                steps.Add(step);
            }

            SimulationStepConfigWindow.Open(this, steps, languages);
        }

        // ── GUI helpers ───────────────────────────────────────────────────────

        private static void DrawWindowTitle(string text)
        {
            EditorGUILayout.LabelField(text,
                new GUIStyle(EditorStyles.boldLabel) { fontSize = 15 }, GUILayout.Height(28));
            EditorGUI.DrawRect(EditorGUILayout.GetControlRect(false, 1),
                new Color(0.45f, 0.45f, 0.45f, 0.7f));
        }

        private static void DrawSectionHeader(string text)
        {
            GUILayout.Space(2);
            EditorGUILayout.LabelField(text, EditorStyles.boldLabel);
        }

        internal static T ObjField<T>(string label, T obj, bool allowScene)
            where T : UnityEngine.Object =>
            (T)EditorGUILayout.ObjectField(label, obj, typeof(T), allowScene);

        internal static T ObjFieldTooltip<T>(string label, string tooltip, T obj, bool allowScene)
            where T : UnityEngine.Object =>
            (T)EditorGUILayout.ObjectField(new GUIContent(label, tooltip), obj, typeof(T), allowScene);
    }


