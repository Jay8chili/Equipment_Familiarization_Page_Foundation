    //  WINDOW 2 — SimulationStepConfigWindow
    // ══════════════════════════════════════════════════════════════════════════

    public class SimulationStepConfigWindow : EditorWindow
    {
        // ── Shared data ───────────────────────────────────────────────────────
        private SimulationStepWizard _wizard;
        internal List<SimToolStepData> _steps;
        internal List<string> _languages;
        private Vector2 _scroll;
        internal bool _justGenerated;

        // ── Deduplication map — source GameObject → already-built Interactions GO ──
        // Populated during GenerateStates, cleared at the start of each run.
        private Dictionary<GameObject, Interactions> _builtInteractions
            = new Dictionary<GameObject, Interactions>();

        // ── Scene picking state ───────────────────────────────────────────────
        private bool _picking = false;
        private int _pickStep = -1;
        private int _pickInteraction = -1;

        private enum PickTarget { Combined, Mesh, Position }
        private PickTarget _pickTarget;

        // Colours
        private static readonly Color COL_PICK_ACTIVE = new Color(0.3f, 0.7f, 1f);

        // ─────────────────────────────────────────────────────────────────────

        public static void Open(
            SimulationStepWizard wizard,
            List<SimToolStepData> steps,
            List<string> languages)
        {
            var w = GetWindow<SimulationStepConfigWindow>("Simulation Step Wizard — Configure Steps");
            w.minSize = new Vector2(560, 680);
            w._wizard = wizard;
            w._steps = steps;
            w._languages = languages;
            w._justGenerated = false;
            w.Repaint();
        }

        // ── Lifecycle ─────────────────────────────────────────────────────────

        private void OnEnable() => SceneView.duringSceneGui += OnSceneGUI;
        private void OnDisable() { SceneView.duringSceneGui -= OnSceneGUI; _picking = false; }

        // ─────────────────────────────────────────────────────────────────────

        private void OnGUI()
        {
            if (_steps == null || _wizard == null)
            {
                EditorGUILayout.HelpBox(
                    "No data loaded. Open the Simulation Step Wizard and load a TSV first.",
                    MessageType.Warning);
                return;
            }

            // Header
            GUILayout.Space(6);
            string langList = _languages?.Count > 0 ? string.Join(", ", _languages) : "—";
            EditorGUILayout.LabelField(
                $"📋  {_steps.Count} step(s)   ·   {(_languages?.Count ?? 0)} language(s): {langList}",
                EditorStyles.miniLabel);
            EditorGUI.DrawRect(EditorGUILayout.GetControlRect(false, 1),
                new Color(0.4f, 0.4f, 0.4f, 0.5f));

            if (_picking)
            {
                EditorGUILayout.HelpBox(
                    "Click an object in the Scene View to pick it. Press Escape to cancel.",
                    MessageType.Warning);
            }

            GUILayout.Space(4);

            // Steps
            _scroll = EditorGUILayout.BeginScrollView(_scroll);
            int removeStep = -1;
            for (int i = 0; i < _steps.Count; i++)
                if (DrawStep(i, _steps[i])) removeStep = i;
            if (removeStep >= 0) { _steps.RemoveAt(removeStep); GUIUtility.ExitGUI(); }
            EditorGUILayout.EndScrollView();

            // Footer
            GUILayout.Space(6);
            if (_justGenerated)
                EditorGUILayout.HelpBox(
                    "✔  States generated successfully! Check your scene hierarchy.",
                    MessageType.Info);

            // ── Add Step button ───────────────────────────────────────────────
            EditorGUILayout.BeginHorizontal();
            GUILayout.FlexibleSpace();
            GUI.color = new Color(0.4f, 0.85f, 0.4f);
            if (GUILayout.Button(new GUIContent("＋  Add Step",
                    "Append a new empty step to the end of the list."),
                    GUILayout.Width(120), GUILayout.Height(26)))
            {
                _steps.Add(new SimToolStepData
                {
                    stepName = $"Step {_steps.Count + 1}",
                    languageTexts = _languages != null
                        ? new List<string>(new string[_languages.Count])
                        : new List<string>()
                });
            }
            GUI.color = Color.white;
            GUILayout.Space(4);
            EditorGUILayout.EndHorizontal();

            GUILayout.Space(4);

            using (new EditorGUI.DisabledGroupScope(_steps.Count == 0))
            {
                var btnStyle = new GUIStyle(GUI.skin.button)
                { fontSize = 13, fontStyle = FontStyle.Bold };
                if (GUILayout.Button("⚡  Generate Simulation States",
                        btnStyle, GUILayout.Height(44)))
                    EditorApplication.delayCall += GenerateStates;
            }

            GUILayout.Space(6);
        }

        // ── Draw one step ─────────────────────────────────────────────────────
        // Returns true when the step should be removed.

        private bool DrawStep(int stepIdx, SimToolStepData step)
        {
            EditorGUILayout.BeginVertical(EditorStyles.helpBox);

            // ── Step header row with remove button ────────────────────────────
            EditorGUILayout.BeginHorizontal();
            step.foldout = EditorGUILayout.Foldout(
                step.foldout,
                $"  Step {stepIdx + 1}  —  {step.stepName}",
                true,
                new GUIStyle(EditorStyles.foldout)
                { fontStyle = FontStyle.Bold, fontSize = 11 });

            GUILayout.FlexibleSpace();

            // ── Add step below button ─────────────────────────────────────────
            GUI.color = new Color(0.4f, 0.8f, 0.4f);
            if (GUILayout.Button(new GUIContent("＋", "Insert a new step below this one."),
                    GUILayout.Width(24), GUILayout.Height(18)))
            {
                _steps.Insert(stepIdx + 1, new SimToolStepData
                {
                    stepName = $"Step {stepIdx + 2}",
                    languageTexts = _languages != null
                        ? new List<string>(new string[_languages.Count])
                        : new List<string>()
                });
                GUI.color = Color.white;
                EditorGUILayout.EndHorizontal();
                EditorGUILayout.EndVertical();
                GUILayout.Space(3);
                GUIUtility.ExitGUI();
                return false;
            }

            // ── Remove step button ────────────────────────────────────────────
            GUI.color = new Color(1f, 0.45f, 0.45f);
            bool removeStep = GUILayout.Button(
                new GUIContent("✕", "Remove this step entirely."),
                GUILayout.Width(22), GUILayout.Height(18));
            GUI.color = Color.white;

            EditorGUILayout.EndHorizontal();

            if (removeStep)
            {
                EditorGUILayout.EndVertical();
                GUILayout.Space(3);
                return true;
            }

            if (step.foldout)
            {
                EditorGUI.indentLevel++;

                // ── Localised texts ───────────────────────────────────────────
                EditorGUILayout.LabelField("Localised Text", EditorStyles.boldLabel);
                EditorGUI.indentLevel++;
                if (_languages != null)
                {
                    for (int l = 0; l < _languages.Count; l++)
                    {
                        while (step.languageTexts.Count <= l) step.languageTexts.Add(string.Empty);
                        EditorGUILayout.BeginHorizontal();
                        EditorGUILayout.LabelField(_languages[l], GUILayout.Width(96));
                        step.languageTexts[l] = EditorGUILayout.TextField(step.languageTexts[l]);
                        EditorGUILayout.EndHorizontal();
                    }
                }
                EditorGUI.indentLevel--;
                GUILayout.Space(6);

                // ── Teleport settings ──────────────────────────────────────────
                EditorGUILayout.LabelField("Teleportation", EditorStyles.boldLabel);
                step.teleportOnStart = EditorGUILayout.Toggle(
                    new GUIContent("Teleport On Start",
                        "If enabled, the player is teleported to the Teleport Target before interactions begin for this step."),
                    step.teleportOnStart);
                if (step.teleportOnStart)
                {
                    step.teleportTarget = (Transform)EditorGUILayout.ObjectField(
                        new GUIContent("Teleport Target",
                            "The Transform the player will be moved to when this step starts."),
                        step.teleportTarget, typeof(Transform), true);
                    if (step.teleportTarget == null)
                        EditorGUILayout.HelpBox(
                            "Assign a Teleport Target transform, or disable Teleport On Start.",
                            MessageType.Warning);
                }
                GUILayout.Space(6);

                // ── Interactions ──────────────────────────────────────────────
                EditorGUILayout.LabelField("Interactions", EditorStyles.boldLabel);

                int removeIdx = -1;
                for (int j = 0; j < step.interactions.Count; j++)
                    if (DrawInteraction(stepIdx, j, step.interactions[j])) removeIdx = j;
                if (removeIdx >= 0) step.interactions.RemoveAt(removeIdx);

                GUILayout.Space(4);
                EditorGUILayout.BeginHorizontal();
                GUILayout.FlexibleSpace();
                if (GUILayout.Button("＋  Add Interaction", GUILayout.Width(158), GUILayout.Height(22)))
                    step.interactions.Add(new SimToolInteractionConfig());
                GUILayout.Space(2);
                EditorGUILayout.EndHorizontal();

                EditorGUI.indentLevel--;
            }

            EditorGUILayout.EndVertical();
            GUILayout.Space(3);
            return false;
        }

        // ── Draw one interaction ──────────────────────────────────────────────
        // Returns true when the remove button is pressed.

        private bool DrawInteraction(int stepIdx, int intIdx, SimToolInteractionConfig cfg)
        {
            bool remove = false;

            EditorGUILayout.BeginVertical(GUI.skin.box);

            // ── Header row ────────────────────────────────────────────────────
            EditorGUILayout.BeginHorizontal();
            cfg.foldout = EditorGUILayout.Foldout(
                cfg.foldout,
                (cfg.type == SimToolInteractionType.UI && cfg.staticUI)
                    ? $"  ◈  {stepIdx + 1}_Static_{cfg.type}_{intIdx + 1}"
                    : $"  ◈  {stepIdx + 1}_{cfg.type}_{intIdx + 1}",
                true);
            GUILayout.FlexibleSpace();

            Color prev = GUI.color;
            GUI.color = new Color(1f, 0.45f, 0.45f);
            if (GUILayout.Button("✕", GUILayout.Width(22), GUILayout.Height(18))) remove = true;
            GUI.color = prev;

            EditorGUILayout.EndHorizontal();

            if (cfg.foldout && !remove)
            {
                EditorGUI.indentLevel++;

                // ── Type selector ─────────────────────────────────────────────
                cfg.type = (SimToolInteractionType)EditorGUILayout.EnumPopup(
                    new GUIContent("Type",
                        "Idle: no user input needed — plays audio/bot prompt then auto-advances.\n" +
                        "Detect: player brings objects into a trigger zone.\n" +
                        "Gaze: player looks at an object for the required duration.\n" +
                        "Grab: player picks up a physical object.\n" +
                        "UI: player taps a button or the assistant bot shows a panel."),
                    cfg.type);

                GUILayout.Space(4);

                // ── Type-specific fields ──────────────────────────────────────
                switch (cfg.type)
                {
                    case SimToolInteractionType.Detect:
                        DrawDetectFields(stepIdx, intIdx, cfg.detectData, isGaze: false);
                        break;

                    case SimToolInteractionType.Gaze:
                        DrawDetectFields(stepIdx, intIdx, cfg.detectData, isGaze: true);
                        break;

                    case SimToolInteractionType.Grab:
                        DrawGrabFields(stepIdx, intIdx, cfg.grabData);
                        break;

                    case SimToolInteractionType.UI:
                        DrawUIFields(cfg);
                        break;

                    default:
                        // Idle — just a prefab-warning if missing
                        break;
                }

                // ── Missing-prefab warning (only for prefab-based types) ──────
                bool usesPrefab = cfg.type == SimToolInteractionType.Idle ||
                                  cfg.type == SimToolInteractionType.UI;
                if (usesPrefab && GetPrefabFor(cfg.type) == null)
                    EditorGUILayout.HelpBox(
                        $"No prefab assigned for '{cfg.type}' in the Wizard window.",
                        MessageType.Warning);

                EditorGUI.indentLevel--;
            }

            EditorGUILayout.EndVertical();
            return remove;
        }

