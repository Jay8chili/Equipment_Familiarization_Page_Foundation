        private void DrawPickRow(int stepIdx, int intIdx, string label, string tooltip,
            ref GameObject field, PickTarget target)
        {
            EditorGUILayout.BeginHorizontal();
            var content = string.IsNullOrEmpty(tooltip)
                ? new GUIContent(label)
                : new GUIContent(label, tooltip);
            field = (GameObject)EditorGUILayout.ObjectField(
                content, field, typeof(GameObject), true);

            bool active = IsPickingFor(stepIdx, intIdx, target);
            Color prev = GUI.backgroundColor;
            GUI.backgroundColor = active ? COL_PICK_ACTIVE : Color.white;

            if (GUILayout.Button(new GUIContent(active ? "Cancel" : "Pick",
                    active ? "Cancel scene picking." : "Click to pick this object from the Scene View."),
                    GUILayout.Width(54)))
