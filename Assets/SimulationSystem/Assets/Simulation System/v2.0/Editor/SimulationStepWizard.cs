        //  SHARED BUILD HELPERS
        // ══════════════════════════════════════════════════════════════════════

        // ── CollectMeshSources ────────────────────────────────────────────────
        // Returns every (mesh, transform) pair found on meshObj or its children.
        //
        // Priority:
        //   1. If meshObj itself has a MeshFilter or SkinnedMeshRenderer with a
        //      valid mesh → return that single source; fromChildren = false.
        //   2. Otherwise scan every child (inactive included) for MeshFilters and
        //      SkinnedMeshRenderers → return all of them; fromChildren = true.
        //   3. Nothing found → empty list; fromChildren = false.
        //
        // fromChildren drives the collider sizing strategy in ApplyColliderFromMeshSource.

        private static List<(Mesh mesh, Transform transform)> CollectMeshSources(
            GameObject meshObj, out bool fromChildren)
        {
            var results = new List<(Mesh, Transform)>();

            // ── 1. Direct MeshFilter on root ──────────────────────────────────
            var rootMF = meshObj.GetComponent<MeshFilter>();
            if (rootMF != null && rootMF.sharedMesh != null)
            {
                results.Add((rootMF.sharedMesh, meshObj.transform));
                fromChildren = false;
                return results;
            }

            // ── 2. Direct SkinnedMeshRenderer on root ─────────────────────────
            var rootSMR = meshObj.GetComponent<SkinnedMeshRenderer>();
            if (rootSMR != null && rootSMR.sharedMesh != null)
            {
                results.Add((rootSMR.sharedMesh, meshObj.transform));
                fromChildren = false;
                return results;
            }

            // ── 3. Children (exclude the root itself) ─────────────────────────
            foreach (var mf in meshObj.GetComponentsInChildren<MeshFilter>(includeInactive: true))
            {
                if (mf.gameObject == meshObj) continue;
                if (mf.sharedMesh != null)
                    results.Add((mf.sharedMesh, mf.transform));
            }
            foreach (var smr in meshObj.GetComponentsInChildren<SkinnedMeshRenderer>(includeInactive: true))
            {
                if (smr.gameObject == meshObj) continue;
                if (smr.sharedMesh != null)
                    results.Add((smr.sharedMesh, smr.transform));
            }

            fromChildren = results.Count > 0;
            return results;
        }

        // ── ApplyBoxFromMesh ──────────────────────────────────────────────────
        // Sizes a trigger BoxCollider from the mesh's world-space bounds.
        // Reuses <existing> if provided; otherwise adds a new BoxCollider.

        private static void ApplyBoxFromMesh(GameObject go, Mesh mesh, Vector3 sourceScale,
            BoxCollider existing = null)
        {
            if (mesh == null) { ApplyBoxFallback(go); return; }

            Vector3 worldSize = Vector3.Scale(mesh.bounds.size, sourceScale);
            Vector3 worldCenter = mesh.bounds.center;

            Vector3 ls = go.transform.lossyScale;
            Vector3 localSize = new Vector3(
                Mathf.Approximately(ls.x, 0) ? worldSize.x : worldSize.x / ls.x,
                Mathf.Approximately(ls.y, 0) ? worldSize.y : worldSize.y / ls.y,
                Mathf.Approximately(ls.z, 0) ? worldSize.z : worldSize.z / ls.z);

            Vector3 localCenter = new Vector3(
                Mathf.Approximately(ls.x, 0) ? worldCenter.x : worldCenter.x * sourceScale.x / ls.x,
                Mathf.Approximately(ls.y, 0) ? worldCenter.y : worldCenter.y * sourceScale.y / ls.y,
                Mathf.Approximately(ls.z, 0) ? worldCenter.z : worldCenter.z * sourceScale.z / ls.z);

            var bc = existing != null ? existing : go.AddComponent<BoxCollider>();
            if (bc == null)
            {
                Debug.LogError(
                    $"[Simulation Step Wizard] AddComponent<BoxCollider> failed on '{go.name}'.");
                return;
            }

            Undo.RecordObject(bc, "Resize BoxCollider");
            bc.center = localCenter;
            bc.size = localSize;
            bc.isTrigger = true;

            Debug.Log($"[Simulation Step Wizard] BoxCollider on '{go.name}' " +
                      $"{(existing != null ? "(reused)" : "(new)")}: " +
                      $"center={bc.center}, size={bc.size}.");
        }

        // ── ApplyBoxFromSources ───────────────────────────────────────────────
        // Computes a single BoxCollider enclosing all source mesh world-space bounds.
        // Reuses <existing> if provided; otherwise adds a new BoxCollider.

        private static void ApplyBoxFromSources(
            GameObject go, List<(Mesh mesh, Transform transform)> sources,
            BoxCollider existing = null)
        {
            Bounds combined = WizardVoxelHelper.TransformBounds(
                sources[0].mesh.bounds, sources[0].transform);
            for (int i = 1; i < sources.Count; i++)
                combined.Encapsulate(
                    WizardVoxelHelper.TransformBounds(sources[i].mesh.bounds, sources[i].transform));

            Vector3 localCenter = go.transform.InverseTransformPoint(combined.center);

            Vector3 ls = go.transform.lossyScale;
            Vector3 localSize = new Vector3(
                Mathf.Approximately(ls.x, 0) ? combined.size.x : combined.size.x / ls.x,
                Mathf.Approximately(ls.y, 0) ? combined.size.y : combined.size.y / ls.y,
                Mathf.Approximately(ls.z, 0) ? combined.size.z : combined.size.z / ls.z);

            var bc = existing != null ? existing : go.AddComponent<BoxCollider>();
            if (bc == null)
            {
                Debug.LogError(
                    $"[Simulation Step Wizard] AddComponent<BoxCollider> failed on '{go.name}'.");
                return;
            }

            Undo.RecordObject(bc, "Resize BoxCollider");
            bc.center = localCenter;
            bc.size = localSize;
            bc.isTrigger = true;

            Debug.Log($"[Simulation Step Wizard] Combined BoxCollider on '{go.name}' " +
                      $"{(existing != null ? "(reused)" : "(new)")} " +
                      $"({sources.Count} mesh source(s)): center={bc.center}, size={bc.size}.");
        }

        // ── ApplyBoxFallback: unit BoxCollider trigger ────────────────────────

        private static void ApplyBoxFallback(GameObject go)
        {
            Debug.LogWarning(
                $"[Simulation Step Wizard] No mesh found for '{go.name}' — using default BoxCollider.");
            // Reuse an existing trigger BoxCollider if there is one; otherwise add new.
            var bc = go.GetComponent<BoxCollider>();
            if (bc == null || !bc.isTrigger)
                bc = go.AddComponent<BoxCollider>();
            if (bc != null)
            {
                Undo.RecordObject(bc, "Resize BoxCollider");
                bc.isTrigger = true;
            }
        }

        // ── ApplyVoxelColliders ───────────────────────────────────────────────
        // Generates compound BoxCollider triggers directly on <paramref name="go"/>
        // using the voxel algorithm.  Accepts any number of mesh sources so it
        // correctly handles both a single root mesh and multiple child meshes
        // (all voxelized into one shared grid, giving one unified collider shape).
        // All colliders are placed on the interaction GO itself — no child object.
        //
        // Grid-fill modes (in priority order):
        //   forceSolid=true  → fill interior, no hollow-out. Combined with
        //                      mergeBoxes this yields the MINIMUM box count.
        //   v.hollow=true    → fill then hollow-out → clean surface shell.
        //   else             → raw triangle voxelization (surface only, may
        //                      have gaps).

        private static void ApplyVoxelColliders(
            GameObject go,
            List<(Mesh mesh, Transform transform)> sources,
            SimToolVoxelSettings v,
            bool forceSolid = false)
        {
            if (sources == null || sources.Count == 0) { ApplyBoxFallback(go); return; }

            // Remove any existing trigger BoxColliders first.
            foreach (var old in go.GetComponents<BoxCollider>())
                if (old.isTrigger) Undo.DestroyObjectImmediate(old);

            // ── Combined world-space bounding box across all sources ───────────
            Bounds worldBounds = WizardVoxelHelper.TransformBounds(
                sources[0].mesh.bounds, sources[0].transform);
            for (int i = 1; i < sources.Count; i++)
                worldBounds.Encapsulate(
                    WizardVoxelHelper.TransformBounds(sources[i].mesh.bounds, sources[i].transform));

            float longestAxis = Mathf.Max(worldBounds.size.x, worldBounds.size.y, worldBounds.size.z);
            if (longestAxis <= 0f) { ApplyBoxFallback(go); return; }

            float voxelSize = longestAxis / v.resolution;
            int xC = Mathf.Max(1, Mathf.CeilToInt(worldBounds.size.x / voxelSize));
            int yC = Mathf.Max(1, Mathf.CeilToInt(worldBounds.size.y / voxelSize));
            int zC = Mathf.Max(1, Mathf.CeilToInt(worldBounds.size.z / voxelSize));

            Vector3 gridSize = new Vector3(xC, yC, zC) * voxelSize;
            Vector3 gridOrigin = worldBounds.center - gridSize * 0.5f;

            // ── Voxelize all sources into the shared grid ─────────────────────
            bool[,,] grid = new bool[xC, yC, zC];

            foreach (var (mesh, meshTransform) in sources)
            {
                var tris = mesh.triangles;
                var verts = mesh.vertices;
                var wVerts = new Vector3[verts.Length];
                for (int vi = 0; vi < verts.Length; vi++)
                    wVerts[vi] = meshTransform.TransformPoint(verts[vi]);

                for (int t = 0; t < tris.Length; t += 3)
                {
                    Vector3 a = wVerts[tris[t]];
                    Vector3 b = wVerts[tris[t + 1]];
                    Vector3 c = wVerts[tris[t + 2]];

                    Bounds tb = new Bounds(a, Vector3.zero);
                    tb.Encapsulate(b);
                    tb.Encapsulate(c);

                    int minX = Mathf.Max(0, Mathf.FloorToInt((tb.min.x - gridOrigin.x) / voxelSize));
                    int minY = Mathf.Max(0, Mathf.FloorToInt((tb.min.y - gridOrigin.y) / voxelSize));
                    int minZ = Mathf.Max(0, Mathf.FloorToInt((tb.min.z - gridOrigin.z) / voxelSize));
                    int maxX = Mathf.Min(xC - 1, Mathf.FloorToInt((tb.max.x - gridOrigin.x) / voxelSize));
                    int maxY = Mathf.Min(yC - 1, Mathf.FloorToInt((tb.max.y - gridOrigin.y) / voxelSize));
                    int maxZ = Mathf.Min(zC - 1, Mathf.FloorToInt((tb.max.z - gridOrigin.z) / voxelSize));

                    for (int x = minX; x <= maxX; x++)
                        for (int y = minY; y <= maxY; y++)
                            for (int z = minZ; z <= maxZ; z++)
                            {
                                if (!grid[x, y, z] &&
                                    WizardVoxelHelper.TriBoxOverlap(
                                        gridOrigin + new Vector3((x + 0.5f) * voxelSize,
                                                                 (y + 0.5f) * voxelSize,
                                                                 (z + 0.5f) * voxelSize),
                                        Vector3.one * voxelSize * 0.5f, a, b, c))
                                {
                                    grid[x, y, z] = true;
                                }
                            }
                }
            }

            if (forceSolid)
            {
                // Fill the interior so the grid is a SOLID volume. With
                // mergeBoxes this collapses into the minimum possible number
                // of BoxColliders (a cube → 1 box; an L-shape → 2 boxes; etc.)
                WizardVoxelHelper.FloodFillInterior(grid, xC, yC, zC);
            }
            else if (v.hollow)
            {
                WizardVoxelHelper.FloodFillInterior(grid, xC, yC, zC);
                WizardVoxelHelper.HollowOut(grid, xC, yC, zC);
            }
            // else: keep raw triangle-overlap voxelization (surface only).

            var boxes = v.mergeBoxes
                ? WizardVoxelHelper.GreedyMerge(grid, xC, yC, zC)
                : WizardVoxelHelper.IndividualBoxes(grid, xC, yC, zC);

            if (boxes.Count == 0) { ApplyBoxFallback(go); return; }

            // ── Add BoxColliders directly to the interaction GO ───────────────
            // Convert every world-space voxel box centre into go's local space.
            foreach (var (min, size) in boxes)
            {
                Vector3 worldCenter = gridOrigin + new Vector3(
                    (min.x + size.x * 0.5f) * voxelSize,
                    (min.y + size.y * 0.5f) * voxelSize,
                    (min.z + size.z * 0.5f) * voxelSize);

                Vector3 worldExtents = new Vector3(
                    size.x * voxelSize + v.skinPadding,
                    size.y * voxelSize + v.skinPadding,
                    size.z * voxelSize + v.skinPadding);

                Vector3 localCenter = go.transform.InverseTransformPoint(worldCenter);

                Vector3 ls = go.transform.lossyScale;
                Vector3 localSize = new Vector3(
                    Mathf.Approximately(ls.x, 0) ? worldExtents.x : worldExtents.x / ls.x,
                    Mathf.Approximately(ls.y, 0) ? worldExtents.y : worldExtents.y / ls.y,
                    Mathf.Approximately(ls.z, 0) ? worldExtents.z : worldExtents.z / ls.z);

                var bc = go.AddComponent<BoxCollider>();
                bc.center = localCenter;
                bc.size = localSize;
                bc.isTrigger = true;
            }

            Debug.Log($"[Simulation Step Wizard] Voxel collider on '{go.name}': " +
                      $"{boxes.Count} box(es) from {sources.Count} mesh source(s) " +
                      $"at resolution {v.resolution}.");
        }

        // ── ApplyColliderFromMeshSource ───────────────────────────────────────
        // Generates a trigger collider on <go> sized from <meshObj>'s mesh.
        // Box mode: reuses the first existing trigger BoxCollider on go (or adds
        //           one if none exists). Extra trigger colliders are removed.
        // Voxel mode: removes all existing trigger colliders and adds fresh ones.
        // Does NOT add or modify any component on meshObj itself.
        //
        // <cleanAllBoxColliders>: when true, NON-trigger BoxColliders are also
        //   removed/reused. Set by the Grab path so the prefab's design-time
        //   physical BoxCollider doesn't linger next to the new fitted one.
        // <forceSolidVoxels>: when true (Voxel mode only), the voxel grid is
        //   filled solid before greedy-merge, producing the minimum possible
        //   number of BoxColliders. Set by the Grab path for "fewest voxels".

        private static void ApplyColliderFromMeshSource(
            GameObject go, GameObject meshObj,
            SimToolColliderMode mode, SimToolVoxelSettings voxelSettings,
            bool cleanAllBoxColliders = false,
            bool forceSolidVoxels = false)
        {
            var sources = CollectMeshSources(meshObj, out bool fromChildren);

            if (sources.Count == 0)
            {
                ApplyBoxFallback(go);
                return;
            }

            if (mode == SimToolColliderMode.Voxel)
            {
                foreach (var old in go.GetComponents<Collider>())
                {
                    if (old.isTrigger) { Undo.DestroyObjectImmediate(old); continue; }
                    if (cleanAllBoxColliders && old is BoxCollider)
                        Undo.DestroyObjectImmediate(old);
                }

                ApplyVoxelColliders(go, sources, voxelSettings, forceSolidVoxels);
            }
            else
            {
                BoxCollider existing = null;
                foreach (var c in go.GetComponents<Collider>())
                {
                    bool eligible = c.isTrigger || (cleanAllBoxColliders && c is BoxCollider);
                    if (!eligible) continue;

                    if (c is BoxCollider bc)
                    {
                        if (existing == null)
                            existing = bc;
                        else
                            Undo.DestroyObjectImmediate(c);
                    }
                    else
                    {
                        Undo.DestroyObjectImmediate(c);
                    }
                }

                if (!fromChildren)
                    ApplyBoxFromMesh(go, sources[0].mesh, meshObj.transform.lossyScale,
                        existing);
                else
                    ApplyBoxFromSources(go, sources, existing);
            }
        }

        // ══════════════════════════════════════════════════════════════════════
        //  VISUAL OVERLAY (Detect / Gaze)
        //
        //  Currently the box collider on a Detect / Gaze GO is invisible at
        //  edit time. AddMeshVisualOverlay collects every MeshFilter and
        //  SkinnedMeshRenderer on the user-selected mesh source (root +
        //  descendants), combines them into a SINGLE merged mesh, and assigns
        //  it to a MeshFilter on the interaction GO ITSELF (replacing any
        //  default cube mesh that came from the prefab). The MeshRenderer's
        //  existing materials — which came from the prefab — stay in place,
        //  so the result is a single ghost shape matching the picked object,
        //  rendered with the prefab's zone material.
        //
        //  Notes:
        //   • The visual lives on the interaction root, NOT as a child GO.
        //     The prefab's default cube mesh is replaced, not duplicated.
        //   • The visual is independent of collider mode — Box or Voxel both
        //     get the SAME visual (one combined mesh). Only the trigger
        //     geometry differs.
        //   • All sub-meshes are merged into a single material slot — perfect
        //     for a uniformly-coloured zone material.
        //   • SkinnedMeshRenderers are baked at their current pose; the
        //     ghost does NOT animate.
        //   • Re-running generation overwrites the mesh on the root and also
        //     removes any legacy child overlays from earlier versions.
        // ══════════════════════════════════════════════════════════════════════

        private const string VISUAL_PREFIX = "__GeneratedVisual_";

        // ── AddMeshVisualOverlay ──────────────────────────────────────────────
        // Combines every MeshFilter and SkinnedMeshRenderer found on <meshObj>
        // (root + descendants, inactive included) into a SINGLE mesh, then
        // assigns that mesh DIRECTLY to a MeshFilter on <go> itself — the
        // interaction root. Any existing mesh on <go> (e.g. the prefab's
        // default cube) is replaced.
        //
        // Material handling:
        //   • If <go> already has a MeshRenderer (it was instantiated from a
        //     prefab that has one on its root) the existing sharedMaterials
        //     are kept — those ARE the prefab's "detect / gaze zone" materials.
        //   • If <go> has no MeshRenderer yet, one is added and its material
        //     is sourced from <materialPrefab>'s first Renderer as a fallback.
        //
        // Behaviour notes:
        //   • Vertex math is done in <go>'s LOCAL space, so the visual lines
        //     up correctly even when the mesh source is at a different world
        //     position than <go> (separate-source mode).
        //   • All sub-meshes are merged into one material slot — perfect for
        //     a uniformly-coloured zone material.
        //   • SkinnedMeshRenderers are baked at their current pose. The
        //     overlay does NOT animate; it is a static snapshot.
        //   • Re-running generation removes any legacy child overlays whose
        //     name starts with VISUAL_PREFIX so old children from previous
        //     versions get cleaned up.

        private static void AddMeshVisualOverlay(
            GameObject go, GameObject meshObj, GameObject materialPrefab, string label,
            float shellOffset = 0f)
        {
            if (go == null) return;

            // ── 1. Clean up any legacy overlay children ───────────────────────
            var toRemove = new List<GameObject>();
            for (int i = 0; i < go.transform.childCount; i++)
            {
                var c = go.transform.GetChild(i);
                if (c.name.StartsWith(VISUAL_PREFIX))
                    toRemove.Add(c.gameObject);
            }
            foreach (var v in toRemove)
                Undo.DestroyObjectImmediate(v);

            if (meshObj == null)
            {
                Debug.LogWarning(
                    $"[Simulation Step Wizard] Visual overlay skipped for '{go.name}': mesh source is null.");
                return;
            }

            // ── 2. Build CombineInstance list in GO's LOCAL space ─────────────
            Matrix4x4 worldToGo = go.transform.worldToLocalMatrix;
            var combine = new List<CombineInstance>();

            // Static MeshFilters
            foreach (var mf in meshObj.GetComponentsInChildren<MeshFilter>(includeInactive: true))
            {
                if (mf.sharedMesh == null) continue;

                Matrix4x4 m = worldToGo * mf.transform.localToWorldMatrix;
                int subCount = mf.sharedMesh.subMeshCount;
                for (int s = 0; s < subCount; s++)
                {
                    combine.Add(new CombineInstance
                    {
                        mesh = mf.sharedMesh,
                        subMeshIndex = s,
                        transform = m
                    });
                }
            }

            // SkinnedMeshRenderers
            foreach (var smr in meshObj.GetComponentsInChildren<SkinnedMeshRenderer>(includeInactive: true))
            {
                if (smr.sharedMesh == null) continue;

                var baked = new Mesh { name = $"BakedTemp_{smr.name}" };
                smr.BakeMesh(baked);

                Matrix4x4 smrWorldNoScale = Matrix4x4.TRS(
                    smr.transform.position, smr.transform.rotation, Vector3.one);
                Matrix4x4 m = worldToGo * smrWorldNoScale;

                int subCount = baked.subMeshCount;
                for (int s = 0; s < subCount; s++)
                {
                    combine.Add(new CombineInstance
                    {
                        mesh = baked,
                        subMeshIndex = s,
                        transform = m
                    });
                }
            }

            if (combine.Count == 0)
            {
                Debug.LogWarning(
                    $"[Simulation Step Wizard] Visual overlay on '{go.name}': mesh source " +
                    $"'{meshObj.name}' contains no MeshFilter or SkinnedMeshRenderer — nothing to render.");
                return;
            }

            // ── 3. Combine all instances into a single mesh ───────────────────
            var combined = new Mesh
            {
                name = $"CombinedVisual_{meshObj.name}",
                indexFormat = UnityEngine.Rendering.IndexFormat.UInt32
            };
            combined.CombineMeshes(combine.ToArray(), mergeSubMeshes: true, useMatrices: true);
            combined.RecalculateBounds();

            // ── 4. Shell offset — extrude vertices along welded normals ──────────
            // RecalculateNormals produces per-triangle normals — shared vertices
            // at hard edges get different normals and pull apart when extruded.
            // Fix: average normals across all vertices at the same position first,
            // then extrude every vertex by that averaged (welded) normal.
            if (shellOffset > 0f)
            {
                combined.RecalculateNormals();
                var verts = combined.vertices;
                var normals = combined.normals;

                // Build position → averaged normal map.
                // Key: vertex position rounded to avoid float precision mismatches.
                var normalSum = new Dictionary<Vector3, Vector3>();
                var normalCount = new Dictionary<Vector3, int>();

                for (int i = 0; i < verts.Length; i++)
                {
                    // Round to 4 decimal places so near-identical positions weld.
                    Vector3 key = new Vector3(
                        Mathf.Round(verts[i].x * 10000f) / 10000f,
                        Mathf.Round(verts[i].y * 10000f) / 10000f,
                        Mathf.Round(verts[i].z * 10000f) / 10000f);

                    if (normalSum.ContainsKey(key))
                    {
                        normalSum[key] += normals[i];
                        normalCount[key] += 1;
                    }
                    else
                    {
                        normalSum[key] = normals[i];
                        normalCount[key] = 1;
                    }
                }

                // Extrude each vertex along its welded (averaged) normal.
                for (int i = 0; i < verts.Length; i++)
                {
                    Vector3 key = new Vector3(
                        Mathf.Round(verts[i].x * 10000f) / 10000f,
                        Mathf.Round(verts[i].y * 10000f) / 10000f,
                        Mathf.Round(verts[i].z * 10000f) / 10000f);

                    Vector3 weldedNormal = (normalSum[key] / normalCount[key]).normalized;
                    verts[i] += weldedNormal * shellOffset;
                }

                combined.vertices = verts;
                combined.RecalculateBounds();
            }

            // ── 4. Assign mesh to a MeshFilter on GO (replace any existing) ───
            var mfTarget = go.GetComponent<MeshFilter>();
            bool addedFilter = false;
            if (mfTarget == null)
            {
                mfTarget = Undo.AddComponent<MeshFilter>(go);
                addedFilter = true;
            }

            Undo.RecordObject(mfTarget, "Set Visual Mesh");
            mfTarget.sharedMesh = combined;

            // ── 5. Ensure GO has a MeshRenderer; only override mats if added ──
            var mrTarget = go.GetComponent<MeshRenderer>();
            bool addedRenderer = false;
            if (mrTarget == null)
            {
                mrTarget = Undo.AddComponent<MeshRenderer>(go);
                addedRenderer = true;

                // No existing renderer on the root — fall back to materials from
                // the prefab so the overlay isn't bright pink.
                Material[] mats = GetMaterialsFromPrefab(materialPrefab);
                if (mats != null && mats.Length > 0)
                {
                    Undo.RecordObject(mrTarget, "Set Visual Material");
                    mrTarget.sharedMaterials = new[] { mats[0] };
                }
                else
                {
                    Debug.LogWarning(
                        $"[Simulation Step Wizard] Visual overlay on '{go.name}': MeshRenderer added " +
                        $"but no material was found on prefab '{(materialPrefab != null ? materialPrefab.name : "<null>")}'. " +
                        "The overlay will render with the default magenta error material.");
                }
            }
            // else: keep the prefab's existing sharedMaterials — those ARE the
            //       detect / gaze zone materials we want.

            Debug.Log($"[Simulation Step Wizard] Visual overlay on '{go.name}': " +
                      $"combined {combine.Count} sub-mesh(es) into 1 mesh ({combined.vertexCount} verts), " +
                      $"assigned to MeshFilter {(addedFilter ? "(added)" : "(reused)")} " +
                      $"with MeshRenderer {(addedRenderer ? "(added)" : "(prefab materials kept)")}.");
        }

        // ── AutoSizeIconCompensator ───────────────────────────────────────────
        // Sets the UniformScaleCompensator on a SPECIFIC child of <go> by index:
        //   • Detect / Gaze → first child  (childIndex = 0)
        //   • Grab           → second child (childIndex = 1)
        //
        // Size is computed DIRECTLY from the mesh — the compensator's <size>
        // field is ignored as input and overwritten with the result. Formula:
        //
        //     size = clamp( sqrt(largestWorldExtent) * ICON_SIZE_COEFF,
        //                   ICON_SIZE_MIN, ICON_SIZE_MAX )
        //
        // Why sqrt: a linear ratio makes icons huge on large meshes and
        // microscopic on small ones. sqrt keeps the icon visible across a
        // wide range of scales while still growing for large objects.
        //
        // Reference points (with current constants):
        //     0.1 m mesh  → 0.15  (clamped to min)
        //     1 m mesh    → 0.30
        //     4 m mesh    → 0.60
        //     11 m mesh   → 0.99
        //     20 m mesh   → 1.00  (clamped to max)
        //
        // <go> is the interaction root. <visualMesh> is the combined visual
        // mesh just placed on go (Detect/Gaze) or the duplicated mesh under
        // the grandchild (Grab). World bounds are derived from the mesh's
        // local-space bounds × <go>'s lossyScale, which matches what the
        // user sees.
        //
        // The compensator's Compensate() is invoked so the icon's localScale
        // picks up the new value immediately at edit-time.

        private const float ICON_SIZE_MIN = 0.2f;
        private const float ICON_SIZE_MAX = 1.5f;
        private const float ICON_SIZE_COEFF = 0.45f;  // tuned so 1 m mesh → 0.45

        private static void AutoSizeIconCompensator(GameObject go, Mesh visualMesh, int childIndex)
        {
            if (go == null) return;
            if (childIndex < 0 || childIndex >= go.transform.childCount)
            {
                Debug.LogWarning(
                    $"[Simulation Step Wizard] Icon auto-size on '{go.name}': child index " +
                    $"{childIndex} is out of range (childCount={go.transform.childCount}). Skipping.");
                return;
            }

            // ── 1. Resolve the compensator on the target child ────────────────
            var targetChild = go.transform.GetChild(childIndex);
            var comp = targetChild.GetComponent<UniformScaleCompensator>();
            if (comp == null)
            {
                Debug.LogWarning(
                    $"[Simulation Step Wizard] Icon auto-size on '{go.name}': child '{targetChild.name}' " +
                    $"(index {childIndex}) has no UniformScaleCompensator. Skipping.");
                return;
            }

            // ── 2. Compute world-space LARGEST dimension of the visual ────────
            float largestWorldExtent;
            if (visualMesh != null)
            {
                Vector3 local = visualMesh.bounds.size;        // mesh local space
                Vector3 ls = go.transform.lossyScale;       // go's world scale
                Vector3 world = new Vector3(
                    Mathf.Abs(local.x * ls.x),
                    Mathf.Abs(local.y * ls.y),
                    Mathf.Abs(local.z * ls.z));
                largestWorldExtent = Mathf.Max(world.x, Mathf.Max(world.y, world.z));
            }
            else
            {
                // Fallback: read trigger-collider bounds from go.
                largestWorldExtent = ComputeLargestColliderExtent(go);
            }

            if (largestWorldExtent <= 0f || float.IsNaN(largestWorldExtent))
            {
                Debug.LogWarning(
                    $"[Simulation Step Wizard] Icon auto-size on '{go.name}': could not " +
                    "determine bounds. Leaving compensator size untouched.");
                return;
            }

            // ── 3. sqrt scaling, clamp to [MIN, MAX], write to compensator ────
            float raw = Mathf.Sqrt(largestWorldExtent) * ICON_SIZE_COEFF;
            float final = Mathf.Clamp(raw, ICON_SIZE_MIN, ICON_SIZE_MAX);

            Undo.RecordObject(comp, "Auto-size Icon Compensator");
            comp.size = final;

            // Persist the change in the scene file.
            EditorUtility.SetDirty(comp);

            // Push the new size through to the icon's localScale.
            comp.Compensate();

            string clampNote =
                raw < ICON_SIZE_MIN ? " (clamped to min)" :
                raw > ICON_SIZE_MAX ? " (clamped to max)" : "";

            Debug.Log(
                $"[Simulation Step Wizard] Icon '{comp.name}' (child {childIndex} of '{go.name}'): " +
                $"largestExtent={largestWorldExtent:F3} → sqrt × {ICON_SIZE_COEFF:F2} = " +
                $"{raw:F3} → size={final:F3}{clampNote}");
        }

        // ── ComputeLargestColliderExtent ──────────────────────────────────────
        // Fallback when no visual mesh is available — uses the union of trigger
        // BoxColliders on <go> in world space. Returns 0 when nothing usable.

        private static float ComputeLargestColliderExtent(GameObject go)
        {
            if (go == null) return 0f;
            var boxes = go.GetComponents<BoxCollider>();
            if (boxes == null || boxes.Length == 0) return 0f;

            bool any = false;
            Bounds combined = default;
            foreach (var bc in boxes)
            {
                if (!bc.isTrigger) continue;
                Vector3 worldCenter = go.transform.TransformPoint(bc.center);
                Vector3 worldSize = Vector3.Scale(bc.size, go.transform.lossyScale);
                worldSize = new Vector3(Mathf.Abs(worldSize.x), Mathf.Abs(worldSize.y), Mathf.Abs(worldSize.z));

                var b = new Bounds(worldCenter, worldSize);
                if (!any) { combined = b; any = true; }
                else combined.Encapsulate(b);
            }
            if (!any) return 0f;
            return Mathf.Max(combined.size.x, Mathf.Max(combined.size.y, combined.size.z));
        }

        // ── GetMaterialsFromPrefab ────────────────────────────────────────────
        // Returns the sharedMaterials of the first Renderer found on <prefab>
        // (root or children, inactive included). Returns null when no Renderer
        // exists or when <prefab> is null.

        private static Material[] GetMaterialsFromPrefab(GameObject prefab)
        {
            if (prefab == null) return null;
            var r = prefab.GetComponentInChildren<Renderer>(includeInactive: true);
            if (r == null) return null;
            var mats = r.sharedMaterials;
            return (mats != null && mats.Length > 0) ? mats : null;
        }

        // ── ParentMeshUnderInteraction ────────────────────────────────────────
        // Re-parents meshObj as a direct child of the interaction GO.
        // No components are added to meshObj. World position is preserved.

        private static void ParentMeshUnderInteraction(GameObject interactionGO, GameObject meshObj)
        {
            if (meshObj == null) return;
            if (PrefabUtility.IsPartOfPrefabAsset(meshObj))
            {
                Debug.LogWarning(
                    $"[Simulation Step Wizard] Mesh source '{meshObj.name}' is a prefab asset — " +
                    "cannot re-parent a prefab asset. Assign a scene instance as the source.");
                return;
            }

            Undo.SetTransformParent(meshObj.transform, interactionGO.transform,
                worldPositionStays: false, "Parent mesh under interaction");

            // Reset local transform so the mesh sits at the interaction GO's origin.
            meshObj.transform.localPosition = Vector3.zero;
            meshObj.transform.localRotation = Quaternion.identity;

            Debug.Log($"[Simulation Step Wizard] Parented '{meshObj.name}' under '{interactionGO.name}'.");
        }

        // ── ParentMeshUnderGrabInteraction ────────────────────────────────────
        // Re-parents meshObj under the first child of interactionGO (grandchild).
        // Skips meshObj itself when searching for the first child (it may already
        // be in the hierarchy). If no suitable child exists, creates a "Mesh" container.
        // No components are added to meshObj. World position is preserved.

        private static void ParentMeshUnderGrabInteraction(GameObject interactionGO, GameObject meshObj)
        {
            if (meshObj == null) return;
            if (PrefabUtility.IsPartOfPrefabAsset(meshObj))
            {
                Debug.LogWarning(
                    $"[Simulation Step Wizard] Mesh source '{meshObj.name}' is a prefab asset — " +
                    "cannot re-parent a prefab asset. Assign a scene instance as the source.");
                return;
            }

            // Capture the world scale we want to preserve BEFORE re-parenting.
            // (For the duplicate this was already baked into localScale as a temp value
            // equal to lossyScale — but we read lossyScale directly to be safe.)
            Vector3 desiredWorldScale = meshObj.transform.lossyScale;

            // Find the first child of interactionGO that is NOT meshObj itself.
            Transform firstChild = null;
            for (int i = 0; i < interactionGO.transform.childCount; i++)
            {
                var c = interactionGO.transform.GetChild(i);
                if (c.gameObject != meshObj)
                {
                    firstChild = c;
                    break;
                }
            }

            if (firstChild == null)
            {
                // No suitable child exists — create an intermediate container.
                var container = new GameObject("Mesh");
                Undo.RegisterCreatedObjectUndo(container, "Create Mesh Container");
                Undo.SetTransformParent(container.transform, interactionGO.transform,
                    worldPositionStays: false, "Parent mesh container");
                firstChild = container.transform;
                Debug.Log($"[Simulation Step Wizard] Created 'Mesh' container under '{interactionGO.name}'.");
            }

            Undo.SetTransformParent(meshObj.transform, firstChild,
                worldPositionStays: false, "Parent mesh under grab grandchild");

            // ── Position meshObj exactly at the interaction GO's WORLD position
            //    (and rotation), regardless of any offset the prefab put on
            //    firstChild. This is what makes the voxel collider land on top
            //    of the grabbable: ApplyVoxelColliders queries this transform
            //    to compute world-space voxel positions. If we zeroed only
            //    localPosition relative to firstChild and firstChild had any
            //    local offset in the prefab, the voxel grid would appear at
            //    that offset distance away from go.
            //    (Box mode happens to dodge this because ApplyBoxFromMesh uses
            //    the mesh's local-space bounds and never queries this transform.)
            meshObj.transform.position = interactionGO.transform.position;
            meshObj.transform.rotation = interactionGO.transform.rotation;

            // Correct localScale so the copy's world scale matches the original.
            // formula: localScale = desiredWorldScale / newParent.lossyScale (component-wise)
            Vector3 ps = firstChild.lossyScale;
            meshObj.transform.localScale = new Vector3(
                Mathf.Approximately(ps.x, 0f) ? desiredWorldScale.x : desiredWorldScale.x / ps.x,
                Mathf.Approximately(ps.y, 0f) ? desiredWorldScale.y : desiredWorldScale.y / ps.y,
                Mathf.Approximately(ps.z, 0f) ? desiredWorldScale.z : desiredWorldScale.z / ps.z);

            Debug.Log($"[Simulation Step Wizard] Parented '{meshObj.name}' under '{firstChild.name}' " +
                      $"(grandchild of '{interactionGO.name}'). " +
                      $"localScale corrected to {meshObj.transform.localScale} " +
                      $"to preserve world scale {desiredWorldScale}.");
        }

        /// <summary>Adds a kinematic, non-gravity Rigidbody to <paramref name="go"/>.</summary>
        private static void AddKinematicRigidbody(GameObject go)
        {
            var rb = go.GetComponent<Rigidbody>();
            if (rb == null)
                rb = go.AddComponent<Rigidbody>();


            rb.useGravity = false;
            rb.isKinematic = true;
            rb.interpolation = RigidbodyInterpolation.None;
            rb.collisionDetectionMode = CollisionDetectionMode.ContinuousSpeculative;
        }

        /// <summary>Validates detect/gaze builder data; returns an error string or null.</summary>
        private static string ValidateDetectData(SimToolDetectData d, bool isGaze)
        {
            if (!d.separateSources && d.combinedSource == null)
                return "Assign a Source Object (or pick one from the scene).";

            if (d.separateSources)
            {
                // Change 5: Give a clear, specific error when only one of the two
                // separate-source fields is filled — instead of a generic "assign mesh source".
                if (d.meshSource == null && d.positionSource != null)
                    return "Mesh Source is empty. Either assign a Mesh Source or disable 'Separate Mesh / Position' to use the Position Source as a combined source.";
                if (d.meshSource == null)
                    return "Assign a Mesh Source.";
                if (d.positionSource == null)
                    return "Assign a Position Source.";
            }

            if (!isGaze)
            {
                // In DetectWithHand mode the objects list is irrelevant —
                // fingers are assigned at generation time from Window 1 fields.
                if (d.detectMode == DetectInteractionMode.DetectWithGrab)
                {
                    if (d.detectObjects.Count == 0)
                        return "Add at least one Object To Detect.";
                    for (int i = 0; i < d.detectObjects.Count; i++)
                        if (d.detectObjects[i] == null)
                            return $"Objects To Detect slot [{i}] is empty.";
                }
            }

            return null;
        }

        // ── Register with SimulationManager ──────────────────────────────────

        private void RegisterStateWithManager(SimulationState state)
        {
            var manager = _wizard.simulationManager;
            if (manager == null) return;

            var so = new SerializedObject(manager);
            string[] candidates =
            {
                "simulationStates", "states", "stateList", "StateList",
                "_states", "_simulationStates", "SimulationStates"
            };

            foreach (string name in candidates)
            {
                var prop = so.FindProperty(name);
                if (prop == null || !prop.isArray) continue;

                prop.arraySize++;
                prop.GetArrayElementAtIndex(prop.arraySize - 1).objectReferenceValue = state;
                so.ApplyModifiedPropertiesWithoutUndo();
                EditorUtility.SetDirty(manager);
                return;
            }

            Debug.LogWarning(
                $"[Simulation Step Wizard] Could not find the states list on SimulationManager. " +
                $"Add '{state.name}' to the manager manually.");
        }

