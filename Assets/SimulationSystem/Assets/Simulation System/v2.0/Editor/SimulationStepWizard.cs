        // ── TransformBounds ───────────────────────────────────────────────────
        internal static Bounds TransformBounds(Bounds local, Transform t)
        {
            var corners = new Vector3[8];
            var c = local.center;
            var e = local.extents;
            corners[0] = t.TransformPoint(c + new Vector3(e.x, e.y, e.z));
            corners[1] = t.TransformPoint(c + new Vector3(e.x, e.y, -e.z));
            corners[2] = t.TransformPoint(c + new Vector3(e.x, -e.y, e.z));
            corners[3] = t.TransformPoint(c + new Vector3(e.x, -e.y, -e.z));
            corners[4] = t.TransformPoint(c + new Vector3(-e.x, e.y, e.z));
            corners[5] = t.TransformPoint(c + new Vector3(-e.x, e.y, -e.z));
            corners[6] = t.TransformPoint(c + new Vector3(-e.x, -e.y, e.z));
            corners[7] = t.TransformPoint(c + new Vector3(-e.x, -e.y, -e.z));

            Bounds b = new Bounds(corners[0], Vector3.zero);
            for (int i = 1; i < 8; i++) b.Encapsulate(corners[i]);
            return b;
        }

        // ── TriBoxOverlap (SAT) ───────────────────────────────────────────────
        // Returns true if the AABB [boxCenter ± boxHalf] overlaps triangle (v0,v1,v2).
        internal static bool TriBoxOverlap(Vector3 boxCenter, Vector3 boxHalf,
            Vector3 v0, Vector3 v1, Vector3 v2)
        {
            v0 -= boxCenter; v1 -= boxCenter; v2 -= boxCenter;

            Vector3 e0 = v1 - v0, e1 = v2 - v1, e2 = v0 - v2;

            // 9 edge × axis tests
            float[] ex = { e0.x, e1.x, e2.x };
            float[] ey = { e0.y, e1.y, e2.y };
            float[] ez = { e0.z, e1.z, e2.z };

            Vector3[] verts = { v0, v1, v2 };

            for (int i = 0; i < 3; i++)
            {
                // a = cross(edge[i], world-axis-j) for j in {X,Y,Z}
                // Test aX
                {
                    float a = -ey[i], b2 = ez[i];
                    float p0 = a * verts[0].y + b2 * verts[0].z;
                    float p1 = a * verts[1].y + b2 * verts[1].z;
                    float p2 = a * verts[2].y + b2 * verts[2].z;
                    float r = boxHalf.y * Mathf.Abs(a) + boxHalf.z * Mathf.Abs(b2);
                    float mn = Mathf.Min(p0, p1, p2), mx = Mathf.Max(p0, p1, p2);
                    if (mn > r || mx < -r) return false;
                }
                // aY
                {
                    float a = -ez[i], b2 = ex[i];
                    float p0 = a * verts[0].z + b2 * verts[0].x;
                    float p1 = a * verts[1].z + b2 * verts[1].x;
                    float p2 = a * verts[2].z + b2 * verts[2].x;
                    float r = boxHalf.z * Mathf.Abs(a) + boxHalf.x * Mathf.Abs(b2);
                    float mn = Mathf.Min(p0, p1, p2), mx = Mathf.Max(p0, p1, p2);
                    if (mn > r || mx < -r) return false;
                }
                // aZ
                {
                    float a = -ex[i], b2 = ey[i];
                    float p0 = a * verts[0].x + b2 * verts[0].y;
                    float p1 = a * verts[1].x + b2 * verts[1].y;
                    float p2 = a * verts[2].x + b2 * verts[2].y;
                    float r = boxHalf.x * Mathf.Abs(a) + boxHalf.y * Mathf.Abs(b2);
                    float mn = Mathf.Min(p0, p1, p2), mx = Mathf.Max(p0, p1, p2);
                    if (mn > r || mx < -r) return false;
                }
            }

            // 3 face normals of the AABB
            if (Mathf.Max(v0.x, v1.x, v2.x) < -boxHalf.x || Mathf.Min(v0.x, v1.x, v2.x) > boxHalf.x) return false;
            if (Mathf.Max(v0.y, v1.y, v2.y) < -boxHalf.y || Mathf.Min(v0.y, v1.y, v2.y) > boxHalf.y) return false;
            if (Mathf.Max(v0.z, v1.z, v2.z) < -boxHalf.z || Mathf.Min(v0.z, v1.z, v2.z) > boxHalf.z) return false;

            // Triangle face normal
            Vector3 n = Vector3.Cross(e0, e1);
            float d = Vector3.Dot(n, v0);
            float rr = boxHalf.x * Mathf.Abs(n.x)
                       + boxHalf.y * Mathf.Abs(n.y)
                       + boxHalf.z * Mathf.Abs(n.z);
            return Mathf.Abs(d) <= rr;
        }

        // ── FloodFillInterior ─────────────────────────────────────────────────
        internal static void FloodFillInterior(bool[,,] grid, int xC, int yC, int zC)
        {
            bool[,,] exterior = new bool[xC, yC, zC];
            var queue = new Queue<Vector3Int>();

            for (int x = 0; x < xC; x++)
                for (int y = 0; y < yC; y++)
                    for (int z = 0; z < zC; z++)
                    {
                        if ((x == 0 || x == xC - 1 || y == 0 || y == yC - 1 || z == 0 || z == zC - 1)
                            && !grid[x, y, z])
                        {
                            exterior[x, y, z] = true;
                            queue.Enqueue(new Vector3Int(x, y, z));
                        }
                    }

            Vector3Int[] dirs = {
                Vector3Int.right, Vector3Int.left,
                Vector3Int.up,    Vector3Int.down,
                new Vector3Int(0, 0, 1), new Vector3Int(0, 0, -1)
            };

            while (queue.Count > 0)
            {
                var p = queue.Dequeue();
                foreach (var dir in dirs)
                {
                    var nb = p + dir;
                    if (nb.x < 0 || nb.x >= xC || nb.y < 0 || nb.y >= yC ||
                        nb.z < 0 || nb.z >= zC) continue;
                    if (exterior[nb.x, nb.y, nb.z] || grid[nb.x, nb.y, nb.z]) continue;
                    exterior[nb.x, nb.y, nb.z] = true;
                    queue.Enqueue(nb);
                }
            }

            for (int x = 0; x < xC; x++)
                for (int y = 0; y < yC; y++)
                    for (int z = 0; z < zC; z++)
                        if (!grid[x, y, z] && !exterior[x, y, z])
                            grid[x, y, z] = true;
        }

        // ── HollowOut ─────────────────────────────────────────────────────────
        internal static void HollowOut(bool[,,] grid, int xC, int yC, int zC)
        {
            bool[,,] keep = new bool[xC, yC, zC];
            for (int x = 0; x < xC; x++)
                for (int y = 0; y < yC; y++)
                    for (int z = 0; z < zC; z++)
                    {
                        if (!grid[x, y, z]) continue;
                        if (x == 0 || x == xC - 1 || y == 0 || y == yC - 1 || z == 0 || z == zC - 1 ||
                            !grid[x - 1, y, z] || !grid[x + 1, y, z] ||
                            !grid[x, y - 1, z] || !grid[x, y + 1, z] ||
                            !grid[x, y, z - 1] || !grid[x, y, z + 1])
                            keep[x, y, z] = true;
                    }
            System.Array.Copy(keep, grid, grid.Length);
        }

        // ── GreedyMerge ───────────────────────────────────────────────────────
        // Runs the greedy slab algorithm in all 6 axis orderings (XYZ, XZY,
        // YXZ, YZX, ZXY, ZYX) and returns the result with the fewest boxes.
        // The box count is highly sensitive to which axis is expanded first;
        // trying all 6 costs very little extra time and reliably gives the
        // minimum collider count for the given voxel grid.
        internal static List<(Vector3Int min, Vector3Int size)> GreedyMerge(
            bool[,,] grid, int xC, int yC, int zC)
        {
            int[] counts = { xC, yC, zC };

            int[][] perms = {
                new[]{0,1,2}, new[]{0,2,1},
                new[]{1,0,2}, new[]{1,2,0},
                new[]{2,0,1}, new[]{2,1,0}
            };

            List<(Vector3Int, Vector3Int)> best = null;
            foreach (var p in perms)
            {
                var candidate = GreedyMergeOrdered(grid, counts, p[0], p[1], p[2]);
                if (best == null || candidate.Count < best.Count)
                    best = candidate;
            }
            return best;
        }

        // Greedy slab merge for one specific axis ordering (a0 → a1 → a2).
        // All inputs/outputs use original (X,Y,Z) grid coordinates.
        private static List<(Vector3Int min, Vector3Int size)> GreedyMergeOrdered(
            bool[,,] grid, int[] counts, int a0, int a1, int a2)
        {
            int c0 = counts[a0], c1 = counts[a1], c2 = counts[a2];
            var result = new List<(Vector3Int, Vector3Int)>();
            var visited = new bool[counts[0], counts[1], counts[2]];

            for (int i = 0; i < c0; i++)
                for (int j = 0; j < c1; j++)
                    for (int k = 0; k < c2; k++)
                    {
                        if (!GGet(grid, i, j, k, a0, a1, a2) ||
                             GGet(visited, i, j, k, a0, a1, a2)) continue;

                        // Expand along a0
                        int ei = i;
                        while (ei + 1 < c0 &&
                               GGet(grid, ei + 1, j, k, a0, a1, a2) &&
                              !GGet(visited, ei + 1, j, k, a0, a1, a2)) ei++;

                        // Expand along a1
                        int ej = j;
                        bool canJ = true;
                        while (canJ && ej + 1 < c1)
                        {
                            for (int ii = i; ii <= ei; ii++)
                                if (!GGet(grid, ii, ej + 1, k, a0, a1, a2) ||
                                     GGet(visited, ii, ej + 1, k, a0, a1, a2))
                                { canJ = false; break; }
                            if (canJ) ej++;
                        }

                        // Expand along a2
                        int ek = k;
                        bool canK = true;
                        while (canK && ek + 1 < c2)
                        {
                            for (int ii = i; ii <= ei && canK; ii++)
                                for (int jj = j; jj <= ej && canK; jj++)
                                    if (!GGet(grid, ii, jj, ek + 1, a0, a1, a2) ||
                                         GGet(visited, ii, jj, ek + 1, a0, a1, a2))
                                        canK = false;
                            if (canK) ek++;
                        }

                        // Mark visited
                        for (int ii = i; ii <= ei; ii++)
                            for (int jj = j; jj <= ej; jj++)
                                for (int kk = k; kk <= ek; kk++)
                                    GSet(visited, ii, jj, kk, a0, a1, a2);

                        // Convert permuted-space result back to original (X,Y,Z) coords.
                        int[] mn = new int[3]; mn[a0] = i; mn[a1] = j; mn[a2] = k;
                        int[] sz = new int[3]; sz[a0] = ei - i + 1; sz[a1] = ej - j + 1; sz[a2] = ek - k + 1;
                        result.Add((new Vector3Int(mn[0], mn[1], mn[2]),
                                    new Vector3Int(sz[0], sz[1], sz[2])));
                    }
            return result;
        }

        // Axis-permuted grid read: i0 is the index along axis a0, etc.
        private static bool GGet(bool[,,] g, int i0, int i1, int i2,
                                  int a0, int a1, int a2)
        {
            int[] idx = new int[3];
            idx[a0] = i0; idx[a1] = i1; idx[a2] = i2;
            return g[idx[0], idx[1], idx[2]];
        }

        // Axis-permuted grid write — marks cell as visited (true).
        private static void GSet(bool[,,] g, int i0, int i1, int i2,
                                  int a0, int a1, int a2)
        {
            int[] idx = new int[3];
            idx[a0] = i0; idx[a1] = i1; idx[a2] = i2;
            g[idx[0], idx[1], idx[2]] = true;
        }

        // ── IndividualBoxes ───────────────────────────────────────────────────
        internal static List<(Vector3Int min, Vector3Int size)> IndividualBoxes(
            bool[,,] grid, int xC, int yC, int zC)
        {
            var result = new List<(Vector3Int, Vector3Int)>();
            for (int x = 0; x < xC; x++)
                for (int y = 0; y < yC; y++)
                    for (int z = 0; z < zC; z++)
                        if (grid[x, y, z])
                            result.Add((new Vector3Int(x, y, z), Vector3Int.one));
            return result;
        }
