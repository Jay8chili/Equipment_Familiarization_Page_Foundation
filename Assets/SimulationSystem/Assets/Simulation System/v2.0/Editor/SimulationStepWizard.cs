//  Place inside any Editor/ folder in your project.
//
//  Menu: Simulation ▶ Simulation Step Wizard
//
//  WINDOW 1 – SimulationStepWizard
//      Assign the SimulationManager, parent transforms, prefabs, and a TSV
//      file, then click "Load TSV & Open Step Wizard →".
//
//  TSV FORMAT (tab-separated)
//      Row 0  (header):   StepName  <Language1>  <Language2>  …
//      Row 1+ (data):     <name>    <text1>       <text2>      …
//
//  WINDOW 2 – SimulationStepConfigWindow
//      Every parsed step is shown with its localised text fields.
//      Add as many interactions as needed per step.
//
//      DETECT / GAZE / GRAB interactions expose the full builder UI:
//        • Objects To Detect list (Detect only)
//        • Target Object (the GO that receives the component + collider)
//        • Mesh & Position Source (combined, or separate mesh / position)
//        • Scene-pick buttons for any source field
//        • Gaze: optional Ray Origin transform
//        • Collider Mode:
//            Box   — single BoxCollider sized from mesh bounds × lossyScale (default)
//            Voxel — compound BoxCollider triggers generated by the voxel algorithm,
//                    added directly to the interaction GO (no child object).
//                    Settings: Resolution, Hollow, Merge Adjacent Boxes, Skin Padding.
//
//      UI interaction: "Static UI" checkbox →
//        ticked  = wires Panel / Button / TMP_Text from the UI Prefab
//        unticked = IsBotHandled; AssistantManager drives the UI
//
//      Click "⚡ Generate Simulation States" to build everything in-scene.
