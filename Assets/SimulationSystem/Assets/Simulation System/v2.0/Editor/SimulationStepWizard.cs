        // ══════════════════════════════════════════════════════════════════════
        //  BUILD — GRAB
        // ══════════════════════════════════════════════════════════════════════

        private GrabInteraction BuildGrabInteraction(
            SimToolGrabData g, string stepName, string interName,
            Transform parent, Dictionary<GameObject, Interactions> built = null)
        {
            // ── Validate sources ──────────────────────────────────────────────
            if (!g.separateSources && g.combinedSource == null)
            {
                Debug.LogWarning($"[Simulation Step Wizard] Grab for '{stepName}' skipped: Assign a Source Object.");
                return null;
            }
            if (g.separateSources && g.meshSource == null)
            {
                Debug.LogWarning($"[Simulation Step Wizard] Grab for '{stepName}' skipped: Assign a Mesh Source.");
                return null;
            }
            if (g.separateSources && g.positionSource == null)
            {
                Debug.LogWarning($"[Simulation Step Wizard] Grab for '{stepName}' skipped: Assign a Position Source.");
                return null;
            }

            GameObject meshObj = g.separateSources ? g.meshSource : g.combinedSource;
            GameObject positionObj = g.separateSources ? g.positionSource : g.combinedSource;

            // ── Deduplication check ───────────────────────────────────────────
            if (built != null && meshObj != null && built.TryGetValue(meshObj, out Interactions existing))
            {
                if (existing is GrabInteraction existingGrab)
                {
                    Debug.Log($"[Simulation Step Wizard] Grab '{interName}': source '{meshObj.name}' " +
                              $"already used — reusing '{existing.gameObject.name}'.");
                    return existingGrab;
                }
            }

            // ── 1. Resolve the target GO ──────────────────────────────────────
            //
            // Priority order:
            //   a) targetObject is a scene instance  → use it directly.
            //   b) targetObject is null AND grabPrefab is assigned
            //      → instantiate the prefab into the scene (preserves the prefab's
            //        existing mesh, renderer, GrabInteraction settings, poses, etc.)
            //   c) targetObject is null AND no prefab  → create a bare GO and
            //        AddComponent<GrabInteraction> (last resort).
            //
            // In every case we then apply / replace the trigger collider so its
            // shape matches the chosen mesh source.

            GameObject go = g.targetObject;

            if (go != null && PrefabUtility.IsPartOfPrefabAsset(go))
            {
                // User dragged a prefab asset into the field instead of a scene object.
                // Treat it the same as "no target" — instantiate it.
                Debug.LogWarning(
                    $"[Simulation Step Wizard] Target Object '{go.name}' is a prefab asset — " +
                    "instantiating it as a scene instance instead.");
                go = null;
            }

            if (go == null)
            {
                GameObject grabPrefab = _wizard.grabPrefab;

                if (grabPrefab != null)
                {
                    go = (GameObject)PrefabUtility.InstantiatePrefab(grabPrefab);
                    Undo.RegisterCreatedObjectUndo(go, "Create Grab GO");
                    go.name = interName;
                }
                else
                {
                    go = new GameObject(interName);
                    Undo.RegisterCreatedObjectUndo(go, "Create Grab GO");
                    Debug.LogWarning(
                        $"[Simulation Step Wizard] No Grab Prefab assigned in the Wizard window. " +
                        "A bare GameObject was created — assign the prefab in Window 1 for best results.");
                }
            }
            else
            {
                go.name = interName;
            }

            // ── 2. Parent ─────────────────────────────────────────────────────
            if (parent != null)
            {
                if (PrefabUtility.IsPartOfPrefabAsset(parent.gameObject))
                    Debug.LogWarning(
                        $"[Simulation Step Wizard] Grab Parent '{parent.name}' is a prefab asset — parenting skipped.");
                else
                    go.transform.SetParent(parent, worldPositionStays: false);
            }

            // ── 3. Position / rotation from positionObj ───────────────────────
            go.transform.position = positionObj.transform.position;
            go.transform.rotation = positionObj.transform.rotation;
            // Do NOT overwrite localScale here — the prefab's scale is intentional.

            // ── 4. Duplicate source mesh, rename+disable original ─────────────
            //
            // The original meshObj is renamed to "UsedIn_<interName>" and disabled
            // so it stays in the scene as a reference but is not active.
            // A duplicate of meshObj is created and used as the grandchild of go.

            GameObject meshCopy = null;

            if (meshObj != null && !PrefabUtility.IsPartOfPrefabAsset(meshObj))
            {
                // Capture world scale BEFORE anything changes.
                Vector3 originalWorldScale = meshObj.transform.lossyScale;

                // Duplicate — keeps all components and children intact.
                meshCopy = UnityEngine.Object.Instantiate(meshObj);
                Undo.RegisterCreatedObjectUndo(meshCopy, "Duplicate Mesh Source");
                meshCopy.name = meshObj.name; // copy keeps the original name

                // Rename and disable the original.
                Undo.RecordObject(meshObj, "Rename & Disable Original Mesh Source");
                meshObj.name = $"UsedIn_{interName}";
                meshObj.SetActive(false);

                // Store originalWorldScale on the copy so ParentMeshUnderGrabInteraction
                // can apply the correct localScale after re-parenting.
                meshCopy.transform.localScale = originalWorldScale; // temp — will be corrected after parenting

                Debug.Log($"[Simulation Step Wizard] Original '{meshObj.name}' renamed & disabled. " +
                          $"Copy '{meshCopy.name}' will become the grandchild.");
            }
            else
            {
                // meshObj is null or a prefab asset — fall back to using it directly.
                meshCopy = meshObj;
            }

            // ── 5. Parent the copy as grandchild FIRST ────────────────────────
            //   The voxel and box colliders are sized using meshCopy's
            //   world-space transform. We must re-parent + correct scale here
            //   BEFORE running the collider generator, otherwise the freshly
            //   instantiated meshCopy still has its source's old parent and
            //   produces world bounds at the wrong location (the "grab voxel
            //   doesn't appear at the grabbable" bug).
            ParentMeshUnderGrabInteraction(go, meshCopy);

            // ── 6. Collider on GO, sized from the now-correctly-placed copy ──
            //   cleanAllBoxColliders=true: also reuse/remove the prefab's
            //     non-trigger BoxCollider so it doesn't linger at its
            //     design-time position.
            //   forceSolidVoxels=true: voxel mode fills the grid solid before
            //     greedy-merge → fewest possible BoxColliders.
            ApplyColliderFromMeshSource(
                go, meshCopy, g.colliderMode, g.voxelSettings,
                cleanAllBoxColliders: true,
                forceSolidVoxels: true);

            // ── 6b. Auto-size the icon UniformScaleCompensator on the SECOND
            //        child of go (Grab prefab structure differs from Detect/Gaze).
            //        Grab's "visual" is the duplicated meshCopy now sitting as
            //        a grandchild. Find its first MeshFilter and use that mesh.
            {
                Mesh visualMesh = null;
                if (meshCopy != null)
                {
                    var mfCopy = meshCopy.GetComponentInChildren<MeshFilter>(includeInactive: true);
                    if (mfCopy != null) visualMesh = mfCopy.sharedMesh;
                }
                AutoSizeIconCompensator(go, visualMesh, childIndex: 1);
            }

            // ── 5. Ensure Rigidbody is kinematic ─────────────────────────────
            AddKinematicRigidbody(go);

            // ── 6. Ensure GrabInteraction is present ──────────────────────────
            var grab = go.GetComponent<GrabInteraction>();
            if (grab == null)
            {
                grab = go.AddComponent<GrabInteraction>();
                if (grab == null)
                {
                    Debug.LogError(
                        $"[Simulation Step Wizard] AddComponent<GrabInteraction> returned null on '{go.name}'. " +
                        "Ensure the object is a scene instance, not a prefab asset.");
                    return null;
                }
            }

            // ── Store in deduplication dict ───────────────────────────────────
            if (built != null && meshObj != null && !built.ContainsKey(meshObj))
                built[meshObj] = grab;

            EditorUtility.SetDirty(go);
            Debug.Log($"[Simulation Step Wizard] Built Grab '{go.name}' for step '{stepName}'.");
            return grab;
        }


