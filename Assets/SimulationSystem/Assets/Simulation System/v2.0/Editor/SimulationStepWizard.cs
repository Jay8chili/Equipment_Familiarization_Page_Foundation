        private Interactions BuildPrefabInteraction(
            SimToolInteractionConfig cfg,
            string stepName,
            string interName,
            string primaryText,
            Transform parent)
        {
            // For UI, pick prefab based on mode.
            GameObject prefab;
            if (cfg.type == SimToolInteractionType.UI)
                prefab = cfg.staticUI ? _wizard.staticUIPrefab : _wizard.uiPrefab;
            else
                prefab = GetPrefabFor(cfg.type);

            if (prefab == null)
            {
                Debug.LogWarning(
                    $"[Simulation Step Wizard] Prefab for '{cfg.type}'" +
                    (cfg.type == SimToolInteractionType.UI ? $" ({(cfg.staticUI ? "Static" : "Bot")})" : "") +
                    $" not assigned — skipping interaction on step '{stepName}'.");
                return null;
            }

            var intGO = (GameObject)PrefabUtility.InstantiatePrefab(prefab, parent);
            Undo.RegisterCreatedObjectUndo(intGO, "Create Interaction");
            intGO.name = interName;

            if (cfg.type == SimToolInteractionType.UI)
            {
                ConfigureUIInteraction(intGO, cfg, primaryText);

                // Apply world position from the assigned transform (no rotation).
                if (cfg.staticUI && cfg.staticUIPosition != null)
                {
                    intGO.transform.position = cfg.staticUIPosition.position;
                    Debug.Log($"[Simulation Step Wizard] Static UI '{intGO.name}' positioned at " +
                              $"'{cfg.staticUIPosition.name}'.");
                }
            }
            if (cfg.type == SimToolInteractionType.Idle)
                ConfigureIdleInteraction(intGO, cfg);

            var inter = intGO.GetComponent<Interactions>()
                     ?? intGO.GetComponentInChildren<Interactions>(includeInactive: true);
            if (inter == null)
                Debug.LogWarning(
                    $"[Simulation Step Wizard] No Interactions component found on '{intGO.name}' " +
                    "or its children. The step will be registered but the interaction won't fire.");
            EditorUtility.SetDirty(intGO);
            return inter;
        }

