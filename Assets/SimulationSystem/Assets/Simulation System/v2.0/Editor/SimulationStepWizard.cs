    // ── Collider generation mode ──────────────────────────────────────────────

    public enum SimToolColliderMode
    {
        Box,    // Single BoxCollider sized from mesh bounds (original behaviour)
        Voxel   // Compound BoxColliders generated by the voxel algorithm
    }

    // ── Detect interaction mode ───────────────────────────────────────────────

    public enum DetectInteractionMode
    {
        DetectWithGrab,  // Default — detect objects are grabbed and brought into zone
        DetectWithHand   // Finger-based — index fingers are the detect objects
    }

    // ── Voxel collider settings (shared by Detect, Gaze, Grab) ───────────────

    [Serializable]
    public class SimToolVoxelSettings
    {
        public int resolution = 16;
        public bool hollow = true;
        public bool mergeBoxes = true;
        public float skinPadding = 0.01f;
    }

    // ── Detect / Gaze builder data ────────────────────────────────────────────

    [Serializable]
    public class SimToolDetectData
    {
        // Objects To Detect (Detect only — Gaze detects the GO it lives on)
        public List<GameObject> detectObjects = new List<GameObject>();

        // Whether each detect object should also be set up as a GrabInteraction.
        // Ticked by default — untick if the objects are already grab-configured.
        public bool detectObjectsAreGrabbable = true;

        // Detect mode: grab-based (default) or hand/finger-based.
        public DetectInteractionMode detectMode = DetectInteractionMode.DetectWithGrab;

        // Target object (receives the component + collider)
        public GameObject targetObject;

        // Mesh & Position source
        public bool separateSources = false;
        public GameObject combinedSource;
        public GameObject meshSource;
        public GameObject positionSource;

        // Gaze-only: ray origin (null → Camera.main at runtime)
        public Transform gazeRayOrigin;

        // Collider generation
        public SimToolColliderMode colliderMode = SimToolColliderMode.Box;
        public SimToolVoxelSettings voxelSettings = new SimToolVoxelSettings();
    }

    // ── Grab builder data ─────────────────────────────────────────────────────

    [Serializable]
    public class SimToolGrabData
    {
        // Target object (receives GrabInteraction + collider)
        public GameObject targetObject;

        // Mesh & Position source
        public bool separateSources = false;
        public GameObject combinedSource;
        public GameObject meshSource;
        public GameObject positionSource;

        // Collider generation
        public SimToolColliderMode colliderMode = SimToolColliderMode.Box;
        public SimToolVoxelSettings voxelSettings = new SimToolVoxelSettings();
    }

    // ── Per-interaction config ─────────────────────────────────────────────────

