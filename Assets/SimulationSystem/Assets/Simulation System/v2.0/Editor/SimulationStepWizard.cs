                if (active)
                {
                    _picking = false;
                }
                else
                {
                    _picking = true;
                    _pickStep = stepIdx;
                    _pickInteraction = intIdx;
                    _pickTarget = target;
                }
            }
            GUI.backgroundColor = prev;
            EditorGUILayout.EndHorizontal();
        }

        private bool IsPickingFor(int stepIdx, int intIdx, PickTarget target) =>
            _picking &&
            _pickStep == stepIdx &&
            _pickInteraction == intIdx &&
            _pickTarget == target;

        // ── Scene GUI (picking) ───────────────────────────────────────────────

        private void OnSceneGUI(SceneView sv)
        {
            if (!_picking) return;

            HandleUtility.AddDefaultControl(GUIUtility.GetControlID(FocusType.Passive));
            Event ev = Event.current;

            if (ev.type == EventType.MouseDown && ev.button == 0)
            {
                Ray ray = HandleUtility.GUIPointToWorldRay(ev.mousePosition);
                GameObject picked = Physics.Raycast(ray, out RaycastHit hit)
                    ? hit.collider.gameObject
                    : HandleUtility.PickGameObject(ev.mousePosition, false);

                if (picked != null &&
                    _pickStep < _steps.Count &&
                    _pickInteraction < _steps[_pickStep].interactions.Count)
                {
                    var cfg = _steps[_pickStep].interactions[_pickInteraction];

                    // Route pick result to detectData or grabData depending on type
                    bool isGrab = cfg.type == SimToolInteractionType.Grab;
                    bool sep = isGrab ? cfg.grabData.separateSources : cfg.detectData.separateSources;

                    if (!sep || _pickTarget == PickTarget.Combined)
                    {
                        if (isGrab) cfg.grabData.combinedSource = picked;
                        else cfg.detectData.combinedSource = picked;
                    }
                    else if (_pickTarget == PickTarget.Mesh)
                    {
                        if (isGrab) cfg.grabData.meshSource = picked;
                        else cfg.detectData.meshSource = picked;
                    }
                    else
                    {
                        if (isGrab) cfg.grabData.positionSource = picked;
                        else cfg.detectData.positionSource = picked;
                    }

                    _picking = false;
                    Repaint();
                    ev.Use();
                }
            }

            if (ev.type == EventType.KeyDown && ev.keyCode == KeyCode.Escape)
            {
                _picking = false;
                Repaint();
                ev.Use();
            }
        }


        // ══════════════════════════════════════════════════════════════════════
        //  GENERATION
        // ══════════════════════════════════════════════════════════════════════

        private void GenerateStates()
        {
            // Unsubscribe first — rapid clicks can queue this more than once.
            EditorApplication.delayCall -= GenerateStates;

            if (_wizard == null || _steps == null) return;

            if (_wizard.simulationManager == null)
            {
                EditorUtility.DisplayDialog("Missing Reference",
                    "SimulationManager is not assigned in the Wizard window.", "OK");
                return;
            }

            bool ok = EditorUtility.DisplayDialog(
                "Generate Simulation States",
                $"This will create {_steps.Count} SimulationState(s) in the active scene.\n\nContinue?",
                "Generate", "Cancel");
            if (!ok) return;

            Undo.SetCurrentGroupName("Generate Simulation States");
            int undoGroup = Undo.GetCurrentGroup();
            int statesMade = 0;

            // Fresh deduplication map for this generation run.
            _builtInteractions.Clear();

            int stepNo = 0;
            foreach (var step in _steps)
            {
                stepNo++;
                string primaryText = step.languageTexts.Count > 0
                    ? step.languageTexts[0] : string.Empty;

                // ── SimulationState GO ────────────────────────────────────────
                // Name: stepIndex_EnglishText (first language column is English)
                string englishText = step.languageTexts.Count > 0
                    ? step.languageTexts[0] : string.Empty;
                string safeEnglish = string.IsNullOrWhiteSpace(englishText)
                    ? step.stepName
                    : englishText.Trim();
                string stateGoName = $"{stepNo}_{safeEnglish}";

                var stateGO = new GameObject(stateGoName);
                Undo.RegisterCreatedObjectUndo(stateGO, "Create SimulationState");

                Transform statesRoot = _wizard.statesParent != null
                    ? _wizard.statesParent
                    : _wizard.simulationManager.transform;
                stateGO.transform.SetParent(statesRoot, false);

                var state = stateGO.AddComponent<SimulationState>();
                state.promptText = primaryText;

                // ── Prompt-only step: no interactions → tick MoveToNextStepAfterAudio ──
                // This lets the SimulationState advance automatically once its audio
                // finishes, without waiting for any player interaction.
                bool isPromptStep = step.interactions == null || step.interactions.Count == 0;
                if (isPromptStep)
                {
                    var promptSO = new SerializedObject(state);
                    var moveNextProp = promptSO.FindProperty("MoveToNextStepAfterAudio");
                    if (moveNextProp != null)
                    {
                        moveNextProp.boolValue = true;
                        promptSO.ApplyModifiedPropertiesWithoutUndo();
                        Debug.Log($"[Simulation Step Wizard] Step '{stateGoName}' has no interactions — " +
                                  "marked as prompt step (MoveToNextStepAfterAudio = true).");
                    }
                    else
                    {
                        Debug.LogWarning($"[Simulation Step Wizard] Could not find 'MoveToNextStepAfterAudio' " +
                                         $"on SimulationState for '{stateGoName}'. Set it manually.");
                    }
                }

                // ── Wire teleport settings ─────────────────────────────────────
                if (step.teleportOnStart && step.teleportTarget != null)
                {
                    var stateSO2 = new SerializedObject(state);
                    var teleportOnProp = stateSO2.FindProperty("teleportOnStart");
                    var teleportTargetProp = stateSO2.FindProperty("teleportTarget");
                    if (teleportOnProp != null) teleportOnProp.boolValue = true;
                    if (teleportTargetProp != null) teleportTargetProp.objectReferenceValue = step.teleportTarget;
                    stateSO2.ApplyModifiedPropertiesWithoutUndo();
                }

                // ── Build each interaction ─────────────────────────────────────
                int intIdx = 0;
                foreach (var cfg in step.interactions)
                {
                    Interactions interaction = null;
                    // naming: stepNo_Type_Index (1-based), with "Static" prefix for static UI
                    string interName = (cfg.type == SimToolInteractionType.UI && cfg.staticUI)
                        ? $"{stepNo}_Static_{cfg.type}_{intIdx + 1}"
                        : $"{stepNo}_{cfg.type}_{intIdx + 1}";

                    switch (cfg.type)
                    {
                        case SimToolInteractionType.Detect:
                            interaction = BuildDetectInteraction(
                                cfg.detectData, step.stepName, interName,
                                GetParentFor(cfg.type), _builtInteractions);
                            break;

                        case SimToolInteractionType.Gaze:
                            interaction = BuildGazeInteraction(
                                cfg.detectData, step.stepName, interName,
                                GetParentFor(cfg.type), _builtInteractions);
                            break;

                        case SimToolInteractionType.Grab:
                            interaction = BuildGrabInteraction(
                                cfg.grabData, step.stepName, interName,
                                GetParentFor(cfg.type), _builtInteractions);
                            break;

                        default:
                            interaction = BuildPrefabInteraction(
                                cfg, step.stepName, interName, primaryText, GetParentFor(cfg.type));
                            break;
                    }

                    intIdx++;

                    if (interaction == null) continue;

                    // Register in SimulationState.listOfInteractions
                    var stateSO = new SerializedObject(state);
                    var listProp = stateSO.FindProperty("listOfInteractions");
                    if (listProp != null && listProp.isArray)
                    {
                        listProp.arraySize++;
                        listProp.GetArrayElementAtIndex(listProp.arraySize - 1)
                                .objectReferenceValue = interaction;
                        stateSO.ApplyModifiedPropertiesWithoutUndo();
                    }
                    else
                    {
                        state.listOfInteractions.Add(interaction);
                        EditorUtility.SetDirty(state);
                    }
                }

                EditorUtility.SetDirty(stateGO);
                EditorUtility.SetDirty(state);
                RegisterStateWithManager(state);
                statesMade++;
            }

            Undo.CollapseUndoOperations(undoGroup);
            EditorSceneManager.MarkSceneDirty(EditorSceneManager.GetActiveScene());

            _justGenerated = true;
            Debug.Log($"[Simulation Step Wizard] ✔ Generated {statesMade} SimulationState(s).");
            EditorUtility.DisplayDialog("Generation Complete",
                $"Created {statesMade} SimulationState(s) in the scene.", "OK");

            // ── Kick off audio generation if enabled ──────────────────────────
            if (_wizard.generateAudioOnCreate)
